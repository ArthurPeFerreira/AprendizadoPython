# Concatenando três strings "A", "B" e "C" usando o operador de adição (+)
concatenar = "A" + "B" + "C"
# Imprime a string resultante da concatenação, que será "ABC"
print(concatenar)

# Repetindo a string "A" 10 vezes usando o operador de multiplicação (*)
a_10_vezes = "A" * 10
# Repetindo a string "Luis" 3 vezes usando o operador de multiplicação (*)
luis_3_vezes = "Luis" * 3

# Imprime a string "A" repetida 10 vezes, resultando em "AAAAAAAAAA"
print(a_10_vezes)

# Imprime a string "Luis" repetida 3 vezes, resultando em "LuisLuisLuis"
print(luis_3_vezes)
