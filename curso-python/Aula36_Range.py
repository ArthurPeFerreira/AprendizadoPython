#Range é utilizado para fazer iteraveis numericos
#range -> range(start,stop,step)
#No range o numero que está o stop não é executado 

#Neste caso temos o range(0,10,1)
numeros = range(10)
for i in numeros:
    print(i)

print("\n")

#Neste caso temos o range(5,10,1)
numeros = range(5,10)
for i in numeros:
    print(i)

print("\n")

#Neste caso temos o range(0,100,2)
numeros = range(0,100,2)
for i in numeros:
    print(i)