# Realiza a adição de 20 + 20 e armazena o resultado na variável 'adicao'
adicao = 20 + 20
# Imprime o resultado da adição
print("Adição:", adicao)

# Realiza a subtração de 15 - 12 e armazena o resultado na variável 'subtracao'
subtracao = 15 - 12
# Imprime o resultado da subtração
print("Subtração:", subtracao)

# Realiza a multiplicação de 12 * 3 e armazena o resultado na variável 'multiplicacao'
multiplicacao = 12 * 3
# Imprime o resultado da multiplicação
print("Multiplicação:", multiplicacao)

# Realiza a divisão de 10.0 / 2.2 (resultando em um número de ponto flutuante) e armazena na variável 'divisao'
divisao = 10.0 / 2.2
# Imprime o resultado da divisão
print("Divisão:", divisao)

# Realiza a divisão inteira de 10.0 // 2.2 (descarta a parte decimal do resultado) e armazena na variável 'divisao_inteira'
divisao_inteira = 10.0 // 2.2
# Imprime o resultado da divisão inteira
print("Divisão Inteira:", divisao_inteira)

# Calcula o resto da divisão de 55 por 2 usando o operador módulo (%) e armazena na variável 'resto_divisao'
resto_divisao = 55 % 2
# Imprime o resto da divisão
print("Resto da Divisão:", resto_divisao)

# Realiza a exponenciação de 2 elevado a 10 usando o operador de potência (**) e armazena na variável 'potenciacao'
potenciacao = 2 ** 10
# Imprime o resultado da exponenciação
print("Potência:", potenciacao)
