# Usadas para salvar algo na memória do computador
# PEP8: Recomenda que se inicie variáveis com letra minúscula, podendo ser usados números e underline

# Variável para armazenar o nome completo da pessoa
nome_completo1 = "Arthur Pedro Ferreira"

# Variáveis para armazenar cada parte do nome
nome_1 = "Arthur"
nome_2 = "Pedro"
nome_3 = "Ferreira"

# Variável que armazena o resultado da soma de 2 + 2
soma_dois_mais_2 = 2 + 2

# Variável que armazena a idade da pessoa
idade = 19

# Variável booleana que verifica se a pessoa é maior de idade (true se idade >= 18)
maior_de_idade = idade >= 18

# Imprime o nome completo da pessoa
print("Nome:", nome_completo1)

# Imprime a idade da pessoa
print("Idade:", idade)

# Imprime se a pessoa é maior de idade (True ou False)
print("É Maior de Idade?", maior_de_idade)

# Imprime o nome completo usando as variáveis separadas com espaço entre os nomes
print(nome_1, nome_2, nome_3, sep=" ")

# Imprime o resultado da soma de 2 + 2
print(soma_dois_mais_2)
